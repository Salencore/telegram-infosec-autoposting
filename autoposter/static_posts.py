from __future__ import annotations

from pathlib import Path

from autoposter.content_plan import CalendarEntry


def load_static_post(entry: CalendarEntry, posts_path: Path) -> str | None:
    candidates = [
        posts_path / f"{entry.publish_date:%Y-%m-%d}.md",
        posts_path / f"day-{entry.day:02d}.md",
    ]
    for path in candidates:
        if path.exists():
            return path.read_text(encoding="utf-8").strip()
    return None


def render_static_post(entry: CalendarEntry) -> str:
    body = _body_for_entry(entry)
    return f"{entry.title}\n\n{entry.hook}\n\n{body}\n\n#инфобез #кибербезопасность #бизнес"


